{
    //F write start from here.
    printf("######\n");
    printf("#\n");
    printf("#\n");
    printf("#####\n");
    printf("#\n");
    printf("#\n");
    printf("#\n");
    // F write end from here.

    //C write start from here.
    printf("  ######\n");
    printf("##      ##\n");
    printf("#\n");
    printf("#\n");
    printf("#\n");
    printf("#\n");
    printf("#\n");
    printf("##      ##\n");
    printf("  ######\n");
    //C write end here.

    return 0;
}
